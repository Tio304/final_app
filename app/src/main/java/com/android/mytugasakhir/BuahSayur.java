package com.android.mytugasakhir;

public class BuahSayur {
    private String namebusa;
    private String hargabusa;
    private int photobusa;


    public String getNamebusa() {
        return namebusa;
    }

    public void setNamebusa(String namebusa) {
        this.namebusa = namebusa;
    }

    public String getHargabusa() {
        return hargabusa;
    }

    public void setHargabusa(String hargabusa) {
        this.hargabusa = hargabusa;
    }

    public int getPhotobusa() {
        return photobusa;
    }

    public void setPhotobusa(int photobusa) {
        this.photobusa = photobusa;
    }
}
